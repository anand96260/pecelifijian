Ko Ni a viribaita ga,
Neimami i Vakabula.<br/>
4	Yabaki ga sa bera mai-
N a gauna nuitaki-
Turaga, Ni solia mai
Na ka e dau namaki:
Na Nomuni veivuke tu
E kena veiveisiga,
Na veitokani kei Jisu.
Na kalougata dina.

</p>

